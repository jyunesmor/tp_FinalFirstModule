class Products {
	id = " ";
	title = " ";
	price = " ";
	code = " ";
	thumbnail = " ";
	stock = " ";

	constructor(id, title, price, code, thumbnail, stock) {
		this.id = id;
		this.title = title;
		this.price = price;
		this.code = code;
		this.thumbnail = thumbnail;
		this.stock = stock;
	}
}

module.exports = Products;
